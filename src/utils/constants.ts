export const ONE_DAY_IN_MS = ONE_DAY_IN_SEC * 1000;
export const ONE_MONTH_IN_SEC = ONE_DAY_IN_SEC * 30;
export const ONE_QUATER_IN_SEC = ONE_DAY_IN_SEC * 91.25;
export const ONE_YEAR_IN_SEC = ONE_DAY_IN_SEC * 365;
